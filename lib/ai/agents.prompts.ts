export function resolvePromptKey(agentId: string): string {
  return AGENT_PROMPT_KEYS[agentId] ?? "ai_analyst";
}

export function buildSystemPrompt(
  agentId: string,
  companyData: Record<string, string | number>
): string {
  const promptKey = resolvePromptKey(agentId);
  let prompt = AGENT_PROMPTS[promptKey] ?? AGENT_PROMPTS.ai_analyst;

  Object.entries(companyData).forEach(([key, value]) => {
    prompt = prompt.replaceAll(`{${key}}`, String(value ?? "не указано"));
  });

  return prompt.replace(/\{[a-z_]+\}/g, "не указано");
}

export function getSuggestedQuestions(agentId: string): string[] {
  return SUGGESTED_QUESTIONS[agentId] ?? SUGGESTED_QUESTIONS.analyst;
}
